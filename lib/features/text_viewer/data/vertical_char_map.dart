const verticalCharMap = <String, String>{
  // 句読点
  '。': '︒',
  '、': '︑',
  ',': '︐',
  '.': '.',

  // 括弧（角括弧）
  '「': '﹁',
  '」': '﹂',
  '『': '﹃',
  '』': '﹄',

  // 括弧（丸括弧）
  '（': '︵',
  '）': '︶',
  '(': '︵',
  ')': '︶',

  // 括弧（波括弧）
  '｛': '︷',
  '｝': '︸',

  // 括弧（亀甲括弧）
  '【': '︻',
  '】': '︼',
  '〔': '︹',
  '〕': '︺',
  '〖': '︗',
  '〗': '︘',

  // 括弧（山括弧）
  '〈': '︿',
  '〉': '﹀',
  '《': '︽',
  '》': '︾',

  // 三点リーダー・ダッシュ
  '…': '︙',
  '─': '│',
  '—': '︱',
};

String mapToVerticalChar(String char) {
  return verticalCharMap[char] ?? char;
}
